
#include "lmTimer2.h"


//<<constructor>>
/****************************************************************************/
lmTimer2::lmTimer2()
/****************************************************************************/
{
  
   _status = 0;
   _time1 = 0;
   _time2 = 0;
   _timediff = 0;
   _tick = 0;
  
  
  
}

//<<destructor>>
/****************************************************************************/
lmTimer2::~lmTimer2()
/****************************************************************************/
{
  
  
}


/****************************************************************************/
int lmTimer2::checkTime()
/****************************************************************************/
{
    /*** Time diff evaluation ***/
  
  _time2 = millis() / 1000;
  
  _timediff = _time2 - _time1;
  _time1 = _time2;
  
  _tick += _timediff;
  
  if(_status == 0) {
	  return -1;
  }
  else {
	  return _tick;
  }
  
}

/****************************************************************************/
void lmTimer2::start()
/****************************************************************************/
{
	
	_status = 1;
	_time1 = millis() / 1000;
	_tick = 0;
	
}

/****************************************************************************/
void lmTimer2::stop()
/****************************************************************************/
{
	_status = 0;
	_time1  = 0;
	_time2 = 0;
	_tick = 0;
	
}